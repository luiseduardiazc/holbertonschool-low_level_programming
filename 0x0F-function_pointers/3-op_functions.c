/**
 * op_add - add
 * @a: int parameter
 * @b: int parameter
 * Return: add
 */
int op_add(int a, int b)
{
	return (a + b);
}
/**
 * op_sub - sub
 * @a: int parameter
 * @b: int parameter
 * Return: sub
 */
int op_sub(int a, int b)
{
	return (a - b);
}
/**
 * op_mul - mul
 * @a: int parameter
 * @b: int parameter
 * Return: mul
 */

int op_mul(int a, int b)
{
	return (a * b);
}
/**
 * op_div - div
 * @a: int parameter
 * @b: int parameter
 * Return: div
 */

int op_div(int a, int b)
{
	return (a / b);
}
/**
 * op_mod - mod
 * @a: int parameter
 * @b: int parameter
 * Return: mod
 */

int op_mod(int a, int b)
{
	return (a % b);
}
